bil1   = input("Masukan bilangan pertama :")
bil2   = input("Masukan bilangan kedua :")

#Penjumlahan
hasil = float(bil1) + float(bil2)
hasil_int_tambah = int(hasil)

print("hasil : {0}+{1} adalah {2}".format(bil1,bil2,hasil_int_tambah) )


#Pengurangan
hasil = float(bil1) - float(bil2)
hasil_int_kurang = int(hasil)

print("hasil : {0}-{1} adalah {2}".format(bil1,bil2,hasil_int_kurang) )

#Perkalian
hasil = float(bil1) * float(bil2)
hasil_int_kali = int(hasil)

print("hasil : {0}*{1} adalah {2}".format(bil1,bil2,hasil_int_kali) )



